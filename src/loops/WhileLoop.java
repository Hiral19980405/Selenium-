package loops;

public class WhileLoop {

	public static void main(String[] args) {

		int count = 1;   //Starting Point   - Initialization
		
		while(count<=10)   //Ending Point   - Condition
 		{
			System.out.println(count);
			
			count = count + 1;   //count++   - Increment or decrement
		}
		
	}

}
